    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        String sistema = "";
        String especialidad = "";
        if(rdbWindows.isSelected())
            sistema = rdbWindows.getText();
       if(rdbLinux.isSelected())
            sistema = rdbLinux.getText();
       if(rdbMac.isSelected())
            sistema = rdbMac.getText();
       //++++++++++++++//
       if(chkProgram.isSelected())
            especialidad =especialidad +" "+ chkProgram.getText();
       if(chkDgrafico.isSelected())
            especialidad =especialidad +" "+ chkDgrafico.getText();
       if(chkAdministracion.isSelected())
            especialidad = especialidad +" "+chkAdministracion.getText();
        String horas = lblHoras.getText(); 
        JOptionPane.showMessageDialog(rootPane, 
                "Tu sistema operativo preferido es "+sistema
               + "\ntus especialidades son "+especialidad 
                + "\n y el numero de horas dedicadas al ordenador son " + horas,"Muestra Datos",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnGenerarActionPerformed

